#1. Si una lancha recorrió 375 kilometros a una velocidad 
# constante en un tiempo de 5 horas. 
# ¿Cual fue la velocidad de la lancha?

kilometros_recorridos = 375 
tiempo_de_recorrido = 5

velocidad_de_la_lancha = kilometros_recorridos / tiempo_de_recorrido

print(f"la velocidad de la lancha fue: {velocidad_de_la_lancha}km/h ")